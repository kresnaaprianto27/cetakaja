function render(category) {

    grid.innerHTML = '';

    products[category].forEach(product => {

        grid.innerHTML += `
        <div class="card fade">

            <img
                src="${product.img}"
                alt="${product.name}"
                style="
                    width:100%;
                    height:400px;
                    object-fit:cover;
                    object-position:center;
                    border-radius:20px;
                    display:block;
                    margin-bottom:16px;
                "
            >

            <div class="card-content">

                <h3>${product.name}</h3>

                <div class="price">
                    ${product.price}
                </div>

                <div class="desc">
                    ${product.desc}
                </div>

                <a
                    class="btn-wa"
                    target="_blank"
                    href="https://wa.me/6281234567890?text=Halo%20saya%20ingin%20memesan%20${encodeURIComponent(product.name)}"
                >
                    Pesan via WhatsApp
                </a>

            </div>

        </div>
        `;
    });
}



document.querySelectorAll('.tab-btn').forEach(button => {

    button.addEventListener('click', () => {

        const category =
        button.dataset.category;

        current = category;

        render(category);

        document
        .querySelectorAll('.tab-btn')
        .forEach(btn => {

            btn.classList.remove('active');

        });

        button.classList.add('active');

    });

});

